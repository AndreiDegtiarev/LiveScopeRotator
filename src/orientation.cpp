    float heading = atan2(mag_y_ut, mag_x_ut);

    // Convert heading from radians to degrees
    if(heading < 0)
      heading += 2 * PI;
    heading *= 180.0 / PI;

    // Adjust for difference between true north and magnetic north
    //heading += magnetic_declination_angle;
